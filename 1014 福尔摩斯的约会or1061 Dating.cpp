/*1014 福尔摩斯的约会 / 1061 Dating
大侦探福尔摩斯接到一张奇怪的字条：我们约会吧！ 3485djDkxh4hhGE 2984akDfkkkkggEdsb s&hgsfdk d&Hyscvnm。
大侦探很快就明白了，字条上奇怪的乱码实际上就是约会的时间星期四 14:04，           
因为前面两字符串中第 1 对相同的大写英文字母（大小写有区分）是第 4 个字母 D，代表星期四；
第 2 对相同的字符是 E ，那是第 5 个英文字母，代表一天里的第 14 个钟头（于是一天的 0点到 23点由数字 0 到 9、以及大写字母 A 到 N 表示）；
后面两字符串第 1 对相同的英文字母 s 出现在第 4 个位置（从 0 开始计数）上，代表第 4 分钟。现给定两对字符串，
请帮助福尔摩斯解码得到约会的时间。
输入格式：
输入在 4 行中分别给出 4 个非空、不包含空格、且长度不超过 60 的字符串。
输出格式：
在一行中输出约会的时间，格式为 DAY HH:MM，其中 DAY 是某星期的 3 字符缩写，即 MON 表示星期一，
TUE 表示星期二，WED 表示星期三，THU 表示星期四，FRI 表示星期五，SAT 表示星期六，SUN 表示星期日。
题目输入保证每个测试存在唯一解。
*/
#include<cstdio>
#include<cstring>

int main() { 
	char week[7][5] = {"MON","TUE","WED","THU","FRI","SAT","SUN"}; 
	char str1[65], str2[65], str3[65], str4[65];
	scanf("%s %s %s %s", str1, str2, str3, str4);
	int len1 = strlen(str1);
	int len2 = strlen(str2);
	int len3 = strlen(str3);
	int len4 = strlen(str4);
	int i;
	//寻找str1和str2中第一对相同位置的 A~Z 的大写字母 
	for(i=0; i<len1 && i<len2; i++) {
		if(str1[i] == str2[i]) {
			if(str1[i]>='A' && str1[i]<='G') {
				printf("%s ", week[str1[i]-'A']);
				break;
			}
		}
	}
	i = i + 1;
	//在上面的基础上，往后寻找相同位置的 0~9 或 A~Z 
	for( ; i<len1 && i<len2; i++) {
		if(str1[i] == str2[i]) {
			if(str1[i]>='0'&&str1[i]<='9') {
				printf("%02d:", str1[i]-'0');
				break;
			} else if(str1[i]>='A'&&str1[i]<='N') {
				printf("%02d:", str1[i]-'A'+10);
				break;
			}
		}
	}
	//寻找str3和str4中第一对相同位置的 A~Z或 a~z 的英文字母 
	for(int j=0; j<len3 && j<len4; j++) {
		if(str3[j] == str4[j]) {
			if(str3[j]>='A'&&str3[j]<='Z'
				|| str3[j]>='a'&&str3[i]<='z') {
				printf("%02d\n", j);
				break;
			}
		}
	}
	return 0;
}




